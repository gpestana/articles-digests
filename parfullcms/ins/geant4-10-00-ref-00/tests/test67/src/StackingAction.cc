{}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......
G4ClassificationOfNewTrack StackingAction::ClassifyNewTrack(const G4Track* aTrack)
{
#ifdef G4_USE_ROOT
  G4int parentID = aTrack->GetParentID();
  if (parentID > 0 && aTrack->GetDefinition() == G4Gamma::Definition())
    {
      G4String processName = aTrack->GetCreatorProcess()->GetProcessName();
      ROOTAnalysis::getInstance()->AddSecondary(processName);
    }
#endif

  return G4UserStackingAction::ClassifyNewTrack(aTrack);
}


